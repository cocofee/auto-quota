def classify(bill_name: str, bill_desc: str = "",
             section_title: str = None, province: str = None) -> dict:
    """
    判断一条清单项属于哪个专业（册）

    优先级：
    0. 项目级覆盖（硬规则，如"配管"永远归C4电气）
    1. 分部标题（最可靠，直接从Excel结构来的）
    2. 数据驱动分类（从定额库学习的TF-IDF模型，像老造价师的经验）
    3. 关键词匹配（手写规则，兜底）
    4. 默认返回None（无法判断，全库搜索）

    参数:
        bill_name: 清单项目名称（如"室内给水管道安装"）
        bill_desc: 清单特征描述（如"材质：镀锌钢管\\nDN25\\n丝接"）
        section_title: 清单所在的分部标题（如"给排水工程"），最可靠的来源
        province: 省份名称（数据驱动分类需要知道读哪个定额库），为None时用默认省份

    返回:
        {
            "primary": "C10",              # 主专业册号（可能为None）
            "primary_name": "给排水采暖燃气", # 主专业名称（可能为None）
            "fallbacks": ["C8", "C12"],    # 借用专业列表
            "confidence": "high/medium/low", # 分类置信度
            "reason": "分部标题匹配: 给排水工程", # 判断依据
        }
    """
    # 第0优先：项目级覆盖（基础设施项目的定额归属不随系统变化）
    # 比如"配管"不管在弱电/消防/暖通清单中，永远搜C4电气册
    override_book = _check_item_override(bill_name)
    if override_book:
        fallbacks = BORROW_PRIORITY.get(override_book, ["C12"])
        return {
            "primary": override_book,
            "primary_name": BOOKS[override_book]["name"],
            "fallbacks": fallbacks,
            "confidence": "high",
            "reason": f"项目级覆盖: '{bill_name}' → {BOOKS[override_book]['name']}",
        }

    # 第1优先：分部标题
    if section_title:
        book = parse_section_title(section_title)
        if book:
            fallbacks = BORROW_PRIORITY.get(book, ["C12"])
            return {
                "primary": book,
                "primary_name": BOOKS[book]["name"],
                "fallbacks": fallbacks,
                "confidence": "high",
                "reason": f"分部标题匹配: {section_title}",
            }

    # 第2优先：数据驱动分类（从定额库学习的TF-IDF模型）
    # 像老造价师一样——看过几万条定额后自然知道"过滤器"属于给排水册
    text = f"{bill_name} {bill_desc}".strip()
    if text:
        try:
            from src.book_classifier import BookClassifier
            classifier = BookClassifier.get_instance(province)
            data_result = classifier.classify(text)
            if data_result and data_result.get("primary"):
                # 低置信度时不用数据驱动，让关键词匹配兜底
                # 例："管道"两个字各册得分接近，数据驱动分不清，关键词更靠谱
                if data_result.get("confidence") != "low":
                    return data_result
        except Exception as e:
            # 数据驱动不可用（定额库未导入等），降级到关键词匹配
            logger.debug(f"数据驱动分类跳过: {e}")

    # 第3优先：关键词匹配（手写规则兜底）
    # 合并名称和描述，在里面找关键词
    if text:
        book, score, matched_keyword = _keyword_match(text)
        if book:
            fallbacks = BORROW_PRIORITY.get(book, ["C12"])
            confidence = "high" if score >= 2 else "medium"
            return {
                "primary": book,
                "primary_name": BOOKS[book]["name"],
                "fallbacks": fallbacks,
                "confidence": confidence,
                "reason": f"关键词匹配: '{matched_keyword}' → {BOOKS[book]['name']}",
            }

    # 无法判断：返回None，表示需要全库搜索
    return {
        "primary": None,
        "primary_name": None,
        "fallbacks": [],
        "confidence": "low",
        "reason": "无法判断专业，将全库搜索",
    }


def get_book_from_quota_id(quota_id: str) -> str | None:
    """
    从定额编号中提取册号

    规则：取编号中第一个 '-' 之前的字母+数字部分
    例如：
        C10-1-5  → "C10"
        C4-8-3   → "C4"
        C1-1-100 → "C1"
        C12-3-1  → "C12"

    参数:
        quota_id: 定额编号（如"C10-1-5"）

    返回:
        册号（如"C10"），无法解析时返回None
    """
    if not quota_id:
        return None

    # 通用匹配：字母(1个或多个)+可选数字 在第一个 '-' 之前
    # C10-1-5 → "C10", A-1-1 → "A", SC1-1-1 → "SC1", GY-1 → "GY"
    match = re.match(r'^([A-Za-z]+\d{0,2})-', quota_id)
    if match:
        book = match.group(1).upper()
        if book in BOOKS:
            return book

    # 纯数字前缀：1-2-3 → "C1"（江西/宁夏等省份编码格式，统一加C前缀）
    # 13+ 的册号属于土建/装饰类，保留原始编号（不加C前缀）
    match = re.match(r'^(\d{1,2})-', quota_id)
    if match:
        num = int(match.group(1))
        if 1 <= num <= 12:
            return f"C{num}"
        return str(num)  # 土建/装饰册号，如 13=抹灰, 14=涂料, 15=金属装饰

    return None


def parse_section_title(title: str) -> str | None:
    """
    从分部/小节标题中判断专业

    例如：
        "给排水工程"  → "C10"
        "电气工程"    → "C4"
        "消防工程"    → "C9"
        "通风空调工程" → "C7"
        "第十册 给排水采暖燃气" → "C10"

    参数:
        title: 分部标题文字

    返回:
        册号（如"C10"），无法判断时返回None
    """
    if not title:
        return None

    title = title.strip()

    # 在标题中查找各专业的关键词
    for book_code, keywords in SECTION_KEYWORDS.items():
        for kw in keywords:
            if kw in title:
                return book_code

    return None


def get_book_info(book_code: str) -> dict | None:
    """
    获取指定册的详细信息

    参数:
        book_code: 册号（如"C10"）

    返回:
        {"code": "C10", "name": "给排水采暖燃气", "keywords": [...], "borrow": [...]}
        不存在时返回None
    """
    if book_code not in BOOKS:
        return None

    return {
        "code": book_code,
        "name": BOOKS[book_code]["name"],
        "keywords": BOOKS[book_code]["keywords"],
        "borrow": BORROW_PRIORITY.get(book_code, []),
    }


def get_all_books() -> list[dict]:
    """
    获取所有册的列表

    返回:
        [{"code": "C1", "name": "机械设备安装"}, {"code": "C2", ...}, ...]
    """
    return [
        {"code": code, "name": info["name"]}
        for code, info in BOOKS.items()
    ]


