import sys
import io
sys.stdout = io.TextIOWrapper(sys.stdout.buffer, encoding='utf-8')

import openpyxl
from openpyxl.styles import Font, PatternFill, Alignment, Border, Side

wb = openpyxl.Workbook()
ws = wb.active
ws.title = '电气工程匹配结果'

# 样式定义
header_font_white = Font(bold=True, size=11, color='FFFFFF')
header_fill = PatternFill(start_color='4472C4', end_color='4472C4', fill_type='solid')
green_fill = PatternFill(start_color='C6EFCE', end_color='C6EFCE', fill_type='solid')
yellow_fill = PatternFill(start_color='FFEB9C', end_color='FFEB9C', fill_type='solid')
section_fill = PatternFill(start_color='D9E2F3', end_color='D9E2F3', fill_type='solid')
section_font = Font(bold=True, size=11)
thin_border = Border(
    left=Side(style='thin'), right=Side(style='thin'),
    top=Side(style='thin'), bottom=Side(style='thin')
)

# 表头
headers = ['序号', '项目名称', '项目特征（关键信息）', '单位', '工程量',
           '匹配定额编号', '匹配定额名称', '匹配依据', '置信度']
for col, h in enumerate(headers, 1):
    cell = ws.cell(row=1, column=col, value=h)
    cell.font = header_font_white
    cell.fill = header_fill
    cell.alignment = Alignment(horizontal='center', vertical='center', wrap_text=True)
    cell.border = thin_border

# 列宽
col_widths = [6, 18, 32, 6, 10, 16, 36, 42, 10]
for i, w in enumerate(col_widths, 1):
    ws.column_dimensions[openpyxl.utils.get_column_letter(i)].width = w

row = 2

def add_section(title):
    global row
    ws.merge_cells(start_row=row, start_column=1, end_row=row, end_column=9)
    cell = ws.cell(row=row, column=1, value=title)
    cell.font = section_font
    cell.fill = section_fill
    cell.alignment = Alignment(horizontal='left')
    for c in range(1, 10):
        ws.cell(row=row, column=c).border = thin_border
    row += 1

def add_row(seq, name, feature, unit, qty, quota_id, quota_name, reason, confidence):
    global row
    data = [seq, name, feature, unit, qty, quota_id, quota_name, reason, confidence]
    for col, val in enumerate(data, 1):
        cell = ws.cell(row=row, column=col, value=val)
        cell.border = thin_border
        cell.alignment = Alignment(vertical='center', wrap_text=True)
    # 颜色
    fill = green_fill if confidence == '高' else yellow_fill
    for c in range(1, 10):
        ws.cell(row=row, column=c).fill = fill
    row += 1


